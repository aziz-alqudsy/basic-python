'''
math operation
including +, *, /, -
'''
math_operation = 1 + 3 * 4 / 2 - 2
print(math_operation)

'''
float division
the result is decimal
'''
float_division = 8 / 3
print(float_division)

'''
integer division
the result is just number and rounded
'''
integer_division = 8 // 3
print(integer_division)

'''
modulus
the result is just remainder
'''
modulus = 13 % 5
print(modulus)
